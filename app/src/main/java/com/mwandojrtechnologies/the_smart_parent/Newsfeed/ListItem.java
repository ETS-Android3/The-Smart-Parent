package com.mwandojrtechnologies.the_smart_parent.Newsfeed;

public class ListItem {

    private String head;
    private String desc;

    public ListItem(){

    }

    public ListItem( String desc) {
        this.head = head;
        this.desc = desc;
    }

    public String getHead() {
        return head;
    }

    public String getDesc() {

        return desc;
    }
}
